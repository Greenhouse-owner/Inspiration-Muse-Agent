// ─── Build flat list ──────────────────────────────────────────────────────────

let _c = 0;
function uid() { return `t${(++_c).toString(36)}`; }

export const LOCAL_TAGS: Tag[] = (Object.entries(RAW) as [CreationPath, RawTag[]][])
  .flatMap(([path, items]) =>
    items.map(item => ({
      id: uid(),
      text: item.text,
      path,
      source: 'local' as const,
      stage: item.stage,
    }))
  );

const BY_PATH: Record<CreationPath, Tag[]> = { story: [], character: [], worldview: [] };
for (const t of LOCAL_TAGS) BY_PATH[t.path].push(t);

const BY_PATH_STAGE: Record<CreationPath, Record<FunnelStage, Tag[]>> = {
  story:     { spread: [], stitch: [], narrow: [] },
  character: { spread: [], stitch: [], narrow: [] },
  worldview: { spread: [], stitch: [], narrow: [] },
};
for (const t of LOCAL_TAGS) {
  if (t.stage) BY_PATH_STAGE[t.path][t.stage].push(t);
}

// ─── Stage calculator ─────────────────────────────────────────────────────────

export function calcStage(pathSelectedCount: number): FunnelStage {
  if (pathSelectedCount <= 1) return 'spread';
  if (pathSelectedCount <= 5) return 'stitch';
  return 'narrow';
}

